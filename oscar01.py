import turtle

def draw_square():
    square_size = 100
    turtle.forward(square_size)
    turtle.right(90)
    turtle.forward(square_size)
    turtle.right(90)
    turtle.forward(square_size)
    turtle.right(90)
    turtle.forward(square_size)




def draw_circles():
    turtle.back(60)
    turtle.left(90)
    turtle.left(90)
    turtle.left(90)
    turtle.pu()
    turtle.forward(50)
    turtle.pd()
    turtle.circle(10)
    turtle.pu()
    turtle.back(50)
    turtle.right(90)
    turtle.forward(20)
    turtle.left(90)
    turtle.forward(25)
    turtle.pd()
    turtle.circle(10)
    turtle.pu()
    turtle.back(25)
    turtle.left(90)
    turtle.forward(80)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(20)
    turtle.right(90)
    turtle.forward(25)
    turtle.pd()
    turtle.circle(10)

draw_square()

